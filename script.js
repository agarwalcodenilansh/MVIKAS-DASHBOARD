function renderEddDetail(filterText) {
  const tbody = document.getElementById('edd-detail-body');
  const countEl = document.getElementById('edd-detail-count');
  if (!tbody) return;
  const q = (filterText || '').trim().toLowerCase();
  const rows = q
    ? eddDetail.filter(r => r.name.toLowerCase().includes(q) || r.id.toLowerCase().includes(q) || (r.transporter || '').toLowerCase().includes(q))
    : eddDetail;

  if (countEl) countEl.textContent = `${rows.length} of ${eddDetail.length} orders`;

  if (rows.length === 0) {
    tbody.innerHTML = `<tr><td colspan="5" style="text-align:center;color:#94a3b8;padding:24px">No matching orders.</td></tr>`;
    return;
  }

  tbody.innerHTML = rows.map(r => `<tr>
      <td style="font-weight:600">${r.id}</td>
      <td>${r.name}</td>
      <td>${r.transporter || '—'}</td>
      <td style="color:#dc2626;font-weight:600">${r.edd || '—'}</td>
      <td>${r.reason ? r.reason : '<span style="color:#94a3b8">Not specified</span>'}</td>
    </tr>`).join('');
}
