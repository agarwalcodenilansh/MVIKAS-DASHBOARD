// Top clients for the "Monthly target distribution" chart (by achieved, falling back to target)
function getTopForPeriod(period, n) {
  const rows = clients.map(c => {
    const { achieved, target } = getPeriodStats(c, period);
    return { name: c.name, achieved: achieved || 0, target: target || 0 };
  }).filter(r => r.achieved > 0 || r.target > 0);
  rows.sort((a, b) => (b.achieved || b.target) - (a.achieved || a.target));
  return rows.slice(0, n);
}

// Top clients by % completion (achieved ÷ target) for the selected period — used by the
// "Achieved vs target comparison" chart, so it ranks by who's closest to/over target rather
// than by raw tonnage. Requires both a target and some achieved tonnage to be rankable, and
// re-computes automatically whenever the period filter changes.
function getTopByCompletion(period, n) {
  const rows = clients.map(c => {
    const { achieved, target } = getPeriodStats(c, period);
    return { name: c.name, achieved: achieved || 0, target: target || 0 };
  }).filter(r => r.target > 0 && r.achieved > 0);
  rows.forEach(r => { r.pct = r.achieved / r.target * 100; });
  rows.sort((a, b) => b.pct - a.pct);
  return rows.slice(0, n);
}

