// Pulls the achieved/target pair for a client for whichever period is selected — this single
// filter now drives the client bar list, both charts, and the KAM summary table below.
function getPeriodStats(c, period) {
  if (period === 'full') return { achieved: c.achieved, target: c.target };
  const p = c.periods && c.periods[period];
  return { achieved: p ? p.achieved : null, target: p ? p.target : null };
}

