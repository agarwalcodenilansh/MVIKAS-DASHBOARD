function renderTonnageBars() {
  const barsEl = document.getElementById('tonnage-bars');
  if (!barsEl) return;

  const titleEl = document.getElementById('tonnage-panel-title');
  if (titleEl) titleEl.textContent = PERIOD_LABELS[currentPeriod];

  const withData = clients.map(c => {
    const { achieved, target } = getPeriodStats(c, currentPeriod);
    return { c, achieved, target };
  }).filter(r =>
    (r.achieved !== null && r.achieved !== undefined && r.achieved > 0) ||
    (r.target !== null && r.target !== undefined && r.target > 0)
  );

  withData.sort((a, b) => (b.achieved || b.target || 0) - (a.achieved || a.target || 0));

  if (withData.length === 0) {
    barsEl.innerHTML = `<div class="tonnage-empty">No client data available for the selected period.</div>`;
    return;
  }

  barsEl.innerHTML = withData.map(({ c, achieved, target }) => {
    const hasTarget = target !== null && target !== undefined && target > 0;
    const hasAchieved = achieved !== null && achieved !== undefined && achieved > 0;
    const pct = (hasTarget && hasAchieved) ? Math.round(achieved / target * 100) : null;
    const displayPct = pct !== null ? Math.min(pct, 100) : (hasTarget && !hasAchieved ? 100 : null);
    const barWidth = displayPct !== null ? displayPct + '%' : '100%';
    const barColor = (hasTarget && !hasAchieved) ? '#cbd5e1' : bucketColor(pct);
    const pctLabel = pct !== null ? pct + '%' : '—';
    const newTag = c.isNew ? '<span class="new-tag">New</span>' : '';

    let targetAchieved;
    if (hasTarget && hasAchieved) {
      targetAchieved = `${fmtKg(achieved)} / ${fmtKg(target)} <span style="color:#94a3b8;font-weight:500">kg</span>`;
    } else if (hasTarget && !hasAchieved) {
      targetAchieved = `— / ${fmtKg(target)} <span style="color:#94a3b8;font-weight:500">kg</span> <span style="color:#cbd5e1;font-size:0.65rem">(achieved pending)</span>`;
    } else {
      targetAchieved = `${fmtKg(achieved)} <span style="color:#94a3b8;font-weight:500">kg</span>${currentPeriod !== 'full' ? ' <span style="color:#cbd5e1;font-size:0.65rem">(target pending)</span>' : ''}`;
    }

    return `<div class="client-row">
      <div class="client-name" title="${c.name}"><span class="client-name-text">${c.name}</span>${newTag}</div>
      <div class="client-person">${c.person}</div>
      <div class="prog-bar-wrap">
        <div class="prog-bar" style="width:${barWidth};background:${barColor}"></div>
      </div>
      <div class="pct-text" style="color:${barColor};font-size:0.75rem">${pctLabel}</div>
      <div class="client-tonnage">${targetAchieved}</div>
    </div>`;
  }).join('');
}
